function loadPopularCountries() {
  const container = document.getElementById('popular-countries');
  if (!container) return;
  const popular = COUNTRIES_DATA.countries.slice(0, 6);
  container.innerHTML = popular.map(createCountryCard).join('');
}

function loadAllCountries(region = 'all') {
  const container = document.getElementById('countries-grid');
  if (!container) return;
  let countries = COUNTRIES_DATA.countries;
  if (region !== 'all') {
    countries = countries.filter(c => c.region === region);
  }
  container.innerHTML = countries.map(createCountryCard).join('');

  const observer = new IntersectionObserver((entries) => {
    entries.forEach(entry => {
      if (entry.isIntersecting) {
        entry.target.classList.add('visible');
      }
    });
  }, { threshold: 0.1 });
  container.querySelectorAll('.fade-in').forEach(el => observer.observe(el));
}

function loadCountryPage() {
  const params = new URLSearchParams(window.location.search);
  const countryId = params.get('id');
  if (!countryId) { window.location.href = 'countries.html'; return; }
  const country = COUNTRIES_DATA.countries.find(c => c.id === countryId);
  if (!country) { window.location.href = 'countries.html'; return; }

  document.title = `${country.name} — VisaTourService`;

  document.getElementById('hero-content').innerHTML = `
    <div class="hero-flag-wrap">
      <img src="${country.flagImg}" alt="${country.name}" class="hero-flag">
    </div>
    <h1 class="hero-title">${country.name}</h1>
    <p class="hero-subtitle">${country.visaType}</p>
    <div class="hero-meta">
      <span>⭐ ${country.rating}</span>
      <span>·</span>
      <span>${country.reviews} отзывов</span>
      <span>·</span>
      <span>${country.price}</span>
    </div>
  `;

  document.getElementById('country-description').innerHTML = `
    <p style="line-height: 1.8;">${country.description}</p>
  `;

  // Документы
  document.getElementById('country-documents').innerHTML =
    country.documents.map(d => `<li>${d}</li>`).join('');

  // Инфо
  document.getElementById('visa-type').textContent = country.visaType;
  document.getElementById('visa-price').textContent = country.price;
  document.getElementById('visa-duration').textContent = country.duration;
  document.getElementById('visa-processing').textContent = country.processingTime;

  // Дополнительная информация
  const extraInfo = document.getElementById('country-extra-info');
  if (extraInfo) {
    extraInfo.innerHTML = `
      <div class="country-info-grid">
        <div class="country-info-item">
          <span class="country-info-label">Столица</span>
          <span class="country-info-value">${country.capital}</span>
        </div>
        <div class="country-info-item">
          <span class="country-info-label">Язык</span>
          <span class="country-info-value">${country.language}</span>
        </div>
        <div class="country-info-item">
          <span class="country-info-label">Валюта</span>
          <span class="country-info-value">${country.currency}</span>
        </div>
        <div class="country-info-item">
          <span class="country-info-label">Часовой пояс</span>
          <span class="country-info-value">${country.timezone}</span>
        </div>
        <div class="country-info-item">
          <span class="country-info-label">Лучшее время</span>
          <span class="country-info-value">${country.bestTime}</span>
        </div>
      </div>
    `;
  }

  // Достопримечательности
  const attractions = document.getElementById('country-attractions');
  if (attractions && country.attractions) {
    attractions.innerHTML = country.attractions.map(a => `<li>${a}</li>`).join('');
  }

  // Советы
  const tips = document.getElementById('country-tips');
  if (tips && country.tips) {
    tips.innerHTML = country.tips.map(t => `<li>${t}</li>`).join('');
  }

  // Нюансы визы
  const visaNuances = document.getElementById('country-visa-nuances');
  if (visaNuances && country.visaNuances) {
    visaNuances.innerHTML = country.visaNuances.map(n => `<li>${n}</li>`).join('');
  }

  // Кнопка
  document.getElementById('apply-btn').style.background = 'var(--accent)';
}
